<h1>Svelte Inview</h1>
<p>
  A Svelte action that monitors an element enters or leaves the viewport/parent
  element. Performant and efficient thanks to using Intersection Observer under
  the hood. Can be used in multiple projects including lazy loading images,
  infinite scrolling, playing/pausing the video when in the viewport, tracking
  user behaviour firing link pre-fetching and animations and many many more.
</p>
